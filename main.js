function broken(x){
    return x.split('').map(a => a == "0" ? "1" : "0").join('');
  }


console.log(broken('11000101011010001')) // => 00111010100101110
console.log(broken('10010010011001000101011010001')) // => 01101101100110111010100101110
console.log(broken('010111100001101011101000101')) // => 101000011110010100010111010
